    public void calculate()
    {
        switch(operation)
        {
            case 1:
                sonuc = number + Double.parseDouble(input.getText());
                input.setText(Double.toString(sonuc));
                break;
            case 2:
                 sonuc = number - Double.parseDouble(input.getText());
                input.setText(Double.toString(sonuc));
                break;
            case 3:
                 sonuc = number / Double.parseDouble(input.getText());
                input.setText(Double.toString(sonuc));
                break;
            case 4:
                 sonuc = number * Double.parseDouble(input.getText());
                input.setText(Double.toString(sonuc));
                break;
                
        }
            
    }
   
